OUTPUT_DIR = REPO_ROOT / "research_archive" / "positive_control_phase3"

# ---------------------------------------------------------------------------
# Frozen parameters (per the task's governance instruction and V2 Section 9)
# ---------------------------------------------------------------------------

BASE_SEED = 20260720  # literal, logged here and in decision_log.md; date-based per H3 convention
ALPHA = 0.05
N_INSTRUMENTS = 25
N_DAYS = 463
HORIZON = 20  # h; L_score = h per V2 Section 1.5
BETA = 0.30  # disclosed convention constant, V2 Section 3
Z_975 = 1.959963985  # standard normal 97.5th percentile, closed-form constant

# Section 2a precision derivation (see docs/POSITIVE_CONTROL_VALIDATION_STUDY_PROPOSAL_V2.md
# Section 2a for the full written derivation -- reproduced numerically here):
#   n_min = N_DAYS - 60 = 403  (worst-case pooled-pair count across the checked lag range)
#   SD    = 1 / sqrt(n_min)    (Fisher's asymptotic bound at rho ~= 0, the worst case)
#   R     = ceil((SD / SE_TARGET) ** 2)
SE_TARGET_FIDELITY = 0.01  # half the frozen 0.02 effect-size grid spacing (V2 Section 9)
_N_MIN = N_DAYS - 60
_SD_FIDELITY = 1.0 / math.sqrt(_N_MIN)
R_FIDELITY = math.ceil((_SD_FIDELITY / SE_TARGET_FIDELITY) ** 2)  # frozen: 25

# Full-precision calibration replication count (derived, NOT executed at full scale
# in this pilot -- see pilot_results.md):
N_EFF = N_DAYS / HORIZON  # ~= 23, cited from docs/REFERENCE_RESEARCH_ROADMAP_NEXT.md via V2 Section 1.3
_SD_CALIBRATION = 1.0 / math.sqrt(N_EFF - 1)
SE_TARGET_CALIBRATION = 0.01
P_FULL = math.ceil((_SD_CALIBRATION / SE_TARGET_CALIBRATION) ** 2)  # frozen target: 500

# This pilot's own, explicitly reduced execution scale:
W_GRID_PILOT = [round(0.1 * i, 2) for i in range(11)]  # 0.0 .. 1.0, step 0.1
P_PILOT = 50


# ---------------------------------------------------------------------------
# RNG (V2 Section 8, frozen scheme)
# ---------------------------------------------------------------------------


def derive_seed(base_seed: int, dimension_point_id: str, rho_true: float, stream_tag: str, replication_index: int) -> int:
    seed_material = f"{base_seed}|{dimension_point_id}|{rho_true:.6f}|{stream_tag}|{replication_index}"
    digest = hashlib.sha256(seed_material.encode("utf-8")).digest()[:8]
    return int.from_bytes(digest, "big")


# ---------------------------------------------------------------------------
# Generator (V2 Section 1.2, the unified latent-path construction)
# ---------------------------------------------------------------------------


def generate_latent_paths(n_instruments: int, n_days: int, h: int, beta: float, rng: random.Random) -> list[list[float]]:
    """r[i][t], t = 0 .. n_days + 2h - 1. Padding of h on each side supplies
    the trailing sma window and the leading forward-return window for every
    valid day index d = 0 .. n_days - 1 (t = h + d)."""
    t_total = n_days + 2 * h
    f = [rng.gauss(0.0, 1.0) for _ in range(t_total)]
    sqrt_beta = math.sqrt(beta)
    sqrt_1mb = math.sqrt(1.0 - beta)
    r = []
    for _ in range(n_instruments):
        r.append([sqrt_beta * f[t] + sqrt_1mb * rng.gauss(0.0, 1.0) for t in range(t_total)])
    return r


def compute_series(r: list[list[float]], n_days: int, h: int) -> tuple[list[list[float]], list[list[float]]]:
    """forward_return[i][d] = sum(r[i][t+1 .. t+h]); sma[i][d] = mean(r[i][t-h+1 .. t]);
    t = h + d. Disjoint time support by construction (V2 Section 1.2)."""
    n_instruments = len(r)
    forward_return = [[0.0] * n_days for _ in range(n_instruments)]
    sma = [[0.0] * n_days for _ in range(n_instruments)]
    for i in range(n_instruments):
        series = r[i]
        # prefix sums for O(n_days) computation instead of O(n_days * h)
        cumsum = [0.0] * (len(series) + 1)
        for t, v in enumerate(series):
            cumsum[t + 1] = cumsum[t] + v
        for d in range(n_days):
            t = h + d
            sma[i][d] = (cumsum[t + 1] - cumsum[t - h + 1]) / h
            forward_return[i][d] = cumsum[t + h + 1] - cumsum[t + 1]
    return forward_return, sma


def compute_score(forward_return: list[list[float]], sma: list[list[float]], w: float, n_instruments: int, n_days: int) -> list[list[float]]:
    """score[i][d] = w * rank_d(forward_return) + sqrt(1 - w^2) * rank_d(sma),
    cross-sectional rank within day d (V2 Section 1.2)."""
    score = [[0.0] * n_days for _ in range(n_instruments)]
    coeff = math.sqrt(max(0.0, 1.0 - w * w))
    for d in range(n_days):
        fr_vals = [forward_return[i][d] for i in range(n_instruments)]
        sma_vals = [sma[i][d] for i in range(n_instruments)]
        fr_ranks = rank_average_ties(fr_vals)
        sma_ranks = rank_average_ties(sma_vals)
        for i in range(n_instruments):
            score[i][d] = w * fr_ranks[i] + coeff * sma_ranks[i]
    return score


def build_panel(forward_return: list[list[float]], score: list[list[float]], n_instruments: int, n_days: int) -> list[dict]:
    """Panel-shaped list of per-day dicts matching significance.py's contract."""
    etf_ids = [f"SYN{i:02d}" for i in range(n_instruments)]
    panel = []
    for d in range(n_days):
        panel.append(
            {
                "etf_ids": etf_ids,
                "score": {etf_ids[i]: score[i][d] for i in range(n_instruments)},
                "forward_return": {etf_ids[i]: forward_return[i][d] for i in range(n_instruments)},
            }
        )
    return panel


def pooled_acf(series_2d: list[list[float]], lag: int) -> float | None:
    """Pooled-across-instruments sample autocorrelation at `lag`: pair each
    (instrument, day) observation with its own lag-ahead value, then Pearson
    correlation of the two pooled vectors (V2 Section 2, gate 1/2 convention)."""
    xs: list[float] = []
    ys: list[float] = []
    for series in series_2d:
        n = len(series)
        for d in range(n - lag):
            xs.append(series[d])
            ys.append(series[d + lag])
    return pearson_correlation(xs, ys)


def series_acf(series: list[float], lag: int) -> float | None:
    n = len(series)
    if n - lag < 2:
        return None
    return pearson_correlation(series[: n - lag], series[lag:])


# ---------------------------------------------------------------------------
# Gate: generator fidelity (V2 Section 2)
# ---------------------------------------------------------------------------


def run_fidelity_gate() -> dict:
    dimension_point_id = f"phase3_fidelity_anchor_n{N_INSTRUMENTS}_d{N_DAYS}_h{HORIZON}_beta{BETA:.2f}"
    w = 0.0  # no injected signal: isolates generator structure from any specific effect size
    rho_true_for_seed = 0.0  # w=0 => population correlation is 0 by construction

    lags_gate12 = list(range(1, HORIZON + 1))
    lags_gate3 = list(range(1, 61))

    return_acf_samples: dict[int, list[float]] = {k: [] for k in lags_gate12}
    score_acf_samples: dict[int, list[float]] = {k: [] for k in lags_gate12}
    ic_acf_samples: dict[int, list[float]] = {k: [] for k in lags_gate3}

    seeds_used = []
    for rep in range(R_FIDELITY):
        seed = derive_seed(BASE_SEED, dimension_point_id, rho_true_for_seed, "panel", rep)
        seeds_used.append(seed)
        rng = random.Random(seed)
        r = generate_latent_paths(N_INSTRUMENTS, N_DAYS, HORIZON, BETA, rng)
        forward_return, sma = compute_series(r, N_DAYS, HORIZON)
        score = compute_score(forward_return, sma, w, N_INSTRUMENTS, N_DAYS)

        for k in lags_gate12:
            fr_acf = pooled_acf(forward_return, k)
            sc_acf = pooled_acf(score, k)
            if fr_acf is not None:
                return_acf_samples[k].append(fr_acf)
            if sc_acf is not None:
                score_acf_samples[k].append(sc_acf)

        panel = build_panel(forward_return, score, N_INSTRUMENTS, N_DAYS)
        ic_series = daily_ic_series(panel, "score")
        for k in lags_gate3:
            v = series_acf(ic_series, k)
            if v is not None:
                ic_acf_samples[k].append(v)

    def summarize(samples: dict[int, list[float]], target_fn) -> dict:
        out = {}
        for k, vals in samples.items():
            n = len(vals)
            mean_val = statistics.fmean(vals) if vals else None
            sd_val = statistics.pstdev(vals) if n > 1 else 0.0
            se_val = sd_val / math.sqrt(n) if n > 0 else None
            tol = Z_975 * se_val if se_val is not None else None
            target = target_fn(k)
            deviation = (mean_val - target) if (mean_val is not None and target is not None) else None
            passed = (abs(deviation) <= tol) if (deviation is not None and tol is not None) else None
            out[str(k)] = {
                "n_replications": n,
                "mean_acf": mean_val,
                "stdev_acf": sd_val,
                "se_mean": se_val,
                "tolerance_95": tol,
                "target": target,
                "deviation": deviation,
                "pass": passed,
            }
        return out

    def bartlett_target(k: int) -> float:
        return max(0.0, 1.0 - k / HORIZON)

    def zero_target_decay_region(k: int):
        return 0.0 if k >= HORIZON else None  # only k >= h is a gated pass condition

    gate1 = summarize(return_acf_samples, bartlett_target)
    gate2 = summarize(score_acf_samples, bartlett_target)
    gate3_full = summarize(ic_acf_samples, lambda k: None)  # informational for all lags
    # apply the decay-region pass condition explicitly for k >= HORIZON
    for k_str, entry in gate3_full.items():
        k = int(k_str)
        if k >= HORIZON and entry["se_mean"] is not None:
            tol = Z_975 * entry["se_mean"]
            entry["tolerance_95"] = tol
            entry["target"] = 0.0
            entry["deviation"] = entry["mean_acf"]
            entry["pass"] = abs(entry["mean_acf"]) <= tol
        else:
            entry["pass"] = None  # not a gated lag (k < h); informational only

    gate1_pass = all(v["pass"] for v in gate1.values() if v["pass"] is not None)
    gate2_pass = all(v["pass"] for v in gate2.values() if v["pass"] is not None)
    gate3_checked = [v["pass"] for k, v in gate3_full.items() if int(k) >= HORIZON]
    gate3_pass = all(gate3_checked) if gate3_checked else None

    return {
        "dimension_point_id": dimension_point_id,
        "anchor_parameters": {
            "n_instruments": N_INSTRUMENTS,
            "n_days": N_DAYS,
            "horizon_h": HORIZON,
            "l_score": HORIZON,
            "beta": BETA,
            "w": w,
        },
        "r_fidelity_replications": R_FIDELITY,
        "tolerance_procedure": {
            "confidence_level": 0.95,
            "z_975": Z_975,
            "se_target": SE_TARGET_FIDELITY,
            "n_min_used_for_derivation": _N_MIN,
            "sd_bound_used_for_derivation": _SD_FIDELITY,
        },
        "seeds_used": seeds_used,
        "gate1_return_side_acf": {"pass": gate1_pass, "by_lag": gate1, "lags_checked": lags_gate12},
        "gate2_score_side_acf": {"pass": gate2_pass, "by_lag": gate2, "lags_checked": lags_gate12},
        "gate3_combined_daily_ic_acf": {
            "pass": gate3_pass,
            "by_lag": gate3_full,
            "lags_checked_for_gate": [k for k in lags_gate3 if k >= HORIZON],
            "lags_reported_informational_only": [k for k in lags_gate3 if k < HORIZON],
        },
        "horizon_sweep_spot_checks_10_20_40": "NOT RUN in this pilot -- anchor (h=20) only. See pilot_results.md limitations.",
        "overall_fidelity_gate_pass": bool(gate1_pass and gate2_pass and gate3_pass) if gate3_pass is not None else None,
    }


# ---------------------------------------------------------------------------
# w <-> rho_true calibration pilot (V2 Section 1.6, reduced pilot scale)
# ---------------------------------------------------------------------------


def run_calibration_pilot() -> list[dict]:
    dimension_point_id = f"phase3_calibration_n{N_INSTRUMENTS}_d{N_DAYS}_h{HORIZON}_beta{BETA:.2f}"
    rows = []
    for w in W_GRID_PILOT:
        estimates = []
        seeds_used = []
        for rep in range(P_PILOT):
            # V2's frozen seed formula's `rho_true` slot is not yet known during
            # calibration (rho_true is the OUTPUT of this procedure, not an input)
            # -- disclosed convention: `w` occupies that slot for calibration runs,
            # distinguished by dimension_point_id. See pilot_results.md RNG section.
            seed = derive_seed(BASE_SEED, dimension_point_id, w, "panel", rep)
            seeds_used.append(seed)
            rng = random.Random(seed)
            r = generate_latent_paths(N_INSTRUMENTS, N_DAYS, HORIZON, BETA, rng)
            forward_return, sma = compute_series(r, N_DAYS, HORIZON)
            score = compute_score(forward_return, sma, w, N_INSTRUMENTS, N_DAYS)
            panel = build_panel(forward_return, score, N_INSTRUMENTS, N_DAYS)
            est = mean_ic(panel, "score")
            if est is not None:
                estimates.append(est)

        n = len(estimates)
        mean_rho = statistics.fmean(estimates) if estimates else None
        sd_rho = statistics.pstdev(estimates) if n > 1 else 0.0
        se_rho = sd_rho / math.sqrt(n) if n > 0 else None
        rows.append(
            {
                "w": w,
                "rho_true_measured_mean": mean_rho,
                "rho_true_measured_stdev": sd_rho,
                "rho_true_measured_se": se_rho,
                "n_replications": n,
                "n_instruments": N_INSTRUMENTS,
                "n_days": N_DAYS,
                "horizon_h": HORIZON,
                "beta": BETA,
                "base_seed": BASE_SEED,
                "dimension_point_id": dimension_point_id,
                "stream_tag": "panel",
                "first_seed": seeds_used[0],
                "last_seed": seeds_used[-1],
            }
        )
    return rows


def main() -> None:
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    fidelity = run_fidelity_gate()
    fidelity["_pilot_size_derivation"] = {
        "r_fidelity_derivation": "R = ceil((SD/SE_TARGET)**2), SD = 1/sqrt(N_DAYS-60), SE_TARGET=0.01",
        "r_fidelity_computed": R_FIDELITY,
        "p_full_calibration_derivation": "P = ceil((SD/SE_TARGET)**2), SD = 1/sqrt(N_DAYS/HORIZON - 1), SE_TARGET=0.01",
        "p_full_calibration_computed": P_FULL,
        "p_pilot_executed_this_run": P_PILOT,
        "w_grid_pilot_executed_this_run": W_GRID_PILOT,
        "w_grid_full_frozen": "41 points, 0.00 to 1.00, step 0.025 (V2 Section 1.6) -- NOT fully executed in this pilot",
    }
    fidelity_path = OUTPUT_DIR / "generator_fidelity_results.json"
    fidelity_path.write_text(json.dumps(fidelity, indent=2, sort_keys=False), encoding="utf-8")
    print(f"Wrote {fidelity_path}")

    calibration_rows = run_calibration_pilot()
    calibration_path = OUTPUT_DIR / "rho_calibration.csv"
    fieldnames = list(calibration_rows[0].keys())
    with calibration_path.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        for row in calibration_rows:
            writer.writerow(row)
    print(f"Wrote {calibration_path}")

    print("\n--- Fidelity gate summary ---")
    print(f"R_FIDELITY = {R_FIDELITY}")
    print(f"gate1 (return-side ACF) pass: {fidelity['gate1_return_side_acf']['pass']}")
    print(f"gate2 (score-side ACF) pass: {fidelity['gate2_score_side_acf']['pass']}")
    print(f"gate3 (combined daily-IC ACF decay) pass: {fidelity['gate3_combined_daily_ic_acf']['pass']}")
    print(f"overall_fidelity_gate_pass: {fidelity['overall_fidelity_gate_pass']}")

    print("\n--- Calibration pilot summary (w, rho_true_measured_mean, se) ---")
    for row in calibration_rows:
        print(f"  w={row['w']:.2f}  rho_hat={row['rho_true_measured_mean']:.5f}  se={row['rho_true_measured_se']:.5f}")

    print(f"\nP_FULL (derived, full-precision requirement) = {P_FULL}; this pilot ran P_PILOT = {P_PILOT} (reduced, disclosed).")


if __name__ == "__main__":
    main()
