"""Positive Control Study -- Phase 3 Pre-validation pilot runner.

Executes two Phase 3 (Pre-validation) activities specified in
docs/POSITIVE_CONTROL_VALIDATION_STUDY_PROPOSAL_V2.md and touches no
real market outcome data at any point -- every input is synthetic,
generated by the frozen latent-path model in that document's Section
1.2:

1. **Generator-fidelity gate (V2 Section 2)**, at the anchor dimension
   point only (25 instruments, 463 days, h=20, beta=0.30), at the
   frozen full pilot size R=25 (derivation: Section 2a of the V2
   document / this script's ``FIDELITY_TOLERANCE`` docstring below).
   Three checks: return-side ACF, score-side ACF (both against the
   closed-form Bartlett(h) target), and the combined daily-IC-series
   ACF decay-to-zero-by-lag-h check.

2. **`w <-> rho_true` calibration pilot (V2 Section 1.6)**, run at a
   deliberately REDUCED, explicitly preliminary scale (11 of the 41
   frozen grid points, 50 of the derived 500 replications per point)
   -- see pilot_results.md for why the full-precision run is not
   executed here and remains a blocker before Methodology Freeze.

RNG (V2 Section 8): every random draw traces to a SHA-256-derived seed,
never Python's built-in hash(). Two named streams exist per the frozen
scheme ("panel", "test"); only "panel" is used by this script, since no
permutation or bootstrap resampling (which would use "test") is run in
Phase 3 pre-validation.

Reuses core/statistics/significance.py's rank_average_ties,
pearson_correlation, daily_ic_series, and mean_ic unmodified -- no
statistical primitive is reimplemented here, only the synthetic
generator itself (which significance.py intentionally knows nothing
about).

Output (research_archive/positive_control_phase3/):
- generator_fidelity_results.json
- rho_calibration.csv
"""

